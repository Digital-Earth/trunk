        public Dictionary<string, int> HashTable = new Dictionary<string, int>();

        public string HashToWords(int hash)
        {
            var color = hash/(Adjectives.Count*Nouns.Count);
            hash = hash % (Adjectives.Count*Nouns.Count);
            var adj = hash/Nouns.Count;
            var noun = hash%Nouns.Count;

            return Colors[color] + " " + Adjectives[adj] + " " + Nouns[noun];
        }


        public void Run(string[] args)
        {
            //make sure the engine is running
            Program.Engine.GetChannel();

            if (args.Length < 2)
            {
                Console.WriteLine("usage: pyx {0} index", Command);
                return;
            }

            PYXIcosIndex index;

            if (args[1] == "random")
            {
                index = new PYXIcosIndex();
                index.randomize(31);
            }
            else
            {
                index = new PYXIcosIndex(args[1]);
            }

            Console.WriteLine(index.toString() + " -> " + SnyderProjection.getInstance().resolutionToPrecision(index.getResolution()) * SphereMath.knEarthRadius);
            
            var hashes = new List<int>();

            var depth = 12;

            while (index.getResolution() > depth)
            {
                var subIndex = index.getSubIndex().toString();
                var postfix = subIndex.Substring(subIndex.Length - depth);
                var child = new PYXIcosIndex(index);
                index.setResolution(index.getResolution()-depth);
                var hash = (int)PYXIcosMath.calcCellPosition(index, child);
                Console.WriteLine(index.toString() + " " + postfix + " " + hash);
                hashes.Add(hash);
            }
            hashes.Add((int)PYXIcosMath.calcCellPosition(index));

            foreach (var hash in hashes.Reverse<int>())
            {
                Console.WriteLine(hash);
                Console.WriteLine(HashToWords(hash));
            }

        }
